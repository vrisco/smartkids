export const SUPPORTED_LANGS = ["es", "en"] as const;
export type Lang = (typeof SUPPORTED_LANGS)[number];

const stored = typeof localStorage !== "undefined" ? localStorage.getItem("sk_lang") : null;
const initialLang: Lang = stored === "en" || stored === "es" ? stored : "es";

